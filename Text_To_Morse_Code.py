text_to_morse_code_dict = {
    "A": "._", "B": "_...", "C": "_._.", "D": "_..", "E": ".", "F": ".._.", "G": "__.", "H": "....", "I": "..",
    "J": ".___", "K": "_._", "L": "._..", "M": "__", "N": "_.", "O": "___", "P": ".__.", "Q": "__._", "R": "._.",
    "S": "...", "T": "_", "U": ".._", "V": "..._", "W": ".__", "X": "_.._", "Y": "_.__", "Z": "__..",1: ".____",
    2: "..___", 3: "...__", 4: "...._", 5: ".....", 6: "_....", 7: "__...", 8: "___..", 9: "____.",0: "_____",
    ".": "._._._", ",": "__..__", "?": "..__..", "!": "_._.__", "'": ".____.", "/": "_.._.", "(": "_.__.",
    ")": "_.__._", "&": "._...", ":": "___...", ";": "_._._.", "=": "_..._", "+": "._._.", "-": "_...._",
    "_": "..__._", "$": "..._.._", "@": ".__._."
}

user_choice = input("Type any text and get the morse code: ").upper()
morse_code = ""

for letters in user_choice:
    if " " in letters:
        morse_code += " "
    else:
        morse_code += text_to_morse_code_dict[letters]
print(f"Morse Code of your Text is: {morse_code}")
